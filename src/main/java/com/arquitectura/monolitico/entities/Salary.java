package com.arquitectura.monolitico.entities;

public class Salary {

  private String rut;
  private String nombre;
  private String apellido;
  private String categoria;
  private int tiempoServicio;
  private String area;
  private int sueldoFijo;
  private int bonoHorasExtras;
  private int bonoTiempoServicio;
  private int bonoPuntualidad;
  private int descuentoIngreso;
  private int descuentoSalida;
  private int sueldoBruto;
  private int prevision;
  private int salud;
  private int sueldoFinal;

  public Salary() {}

  public String getRut() {
    return rut;
  }

  public void setRut(String rut) {
    this.rut = rut;
  }

  public String getNombre() {
    return nombre;
  }

  public void setNombre(String nombre) {
    this.nombre = nombre;
  }

  public String getApellido() {
    return apellido;
  }

  public void setApellido(String apellido) {
    this.apellido = apellido;
  }

  public String getCategoria() {
    return categoria;
  }

  public void setCategoria(String categoria) {
    this.categoria = categoria;
  }

  public int getTiempoServicio() {
    return tiempoServicio;
  }

  public void setTiempoServicio(int tiempoServicio) {
    this.tiempoServicio = tiempoServicio;
  }

  public String getArea() {
    return area;
  }

  public void setArea(String area) {
    this.area = area;
  }

  public int getSueldoFijo() {
    return sueldoFijo;
  }

  public void setSueldoFijo(int sueldoFijo) {
    this.sueldoFijo = sueldoFijo;
  }

  public int getBonoHorasExtras() {
    return bonoHorasExtras;
  }

  public void setBonoHorasExtras(int bonoHorasExtras) {
    this.bonoHorasExtras = bonoHorasExtras;
  }

  public int getBonoTiempoServicio() {
    return bonoTiempoServicio;
  }

  public void setBonoTiempoServicio(int bonoTiempoServicio) {
    this.bonoTiempoServicio = bonoTiempoServicio;
  }

  public int getBonoPuntualidad() {
    return bonoPuntualidad;
  }

  public void setBonoPuntualidad(int bonoPuntualidad) {
    this.bonoPuntualidad = bonoPuntualidad;
  }

  public int getDescuentoIngreso() {
    return descuentoIngreso;
  }

  public void setDescuentoIngreso(int descuentoIngreso) {
    this.descuentoIngreso = descuentoIngreso;
  }

  public int getDescuentoSalida() {
    return descuentoSalida;
  }

  public void setDescuentoSalida(int descuentoSalida) {
    this.descuentoSalida = descuentoSalida;
  }

  public int getSueldoBruto() {
    return sueldoBruto;
  }

  public void setSueldoBruto(int sueldoBruto) {
    this.sueldoBruto = sueldoBruto;
  }

  public int getPrevision() {
    return prevision;
  }

  public void setPrevision(int prevision) {
    this.prevision = prevision;
  }

  public int getSalud() {
    return salud;
  }

  public void setSalud(int salud) {
    this.salud = salud;
  }

  public int getSueldoFinal() {
    return sueldoFinal;
  }

  public void setSueldoFinal(int sueldoFinal) {
    this.sueldoFinal = sueldoFinal;
  }
}
